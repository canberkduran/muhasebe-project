package entitiy;

public class StokKarti{
    private int id;
    private String ad;
    private String birimTuru;

    private int stokId;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAd() {
        return ad;
    }

    public void setAd(String ad) {
        this.ad = ad;
    }

    public String getBirimTuru() {
        return birimTuru;
    }

    public void setBirimTuru(String birimTuru) {
        this.birimTuru = birimTuru;
    }

    public int getStokId() {
        return stokId;
    }

    public void setStokId(int stokId) {
        this.stokId = stokId;
    }
}
